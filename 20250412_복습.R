# T검정(t-test)
# 두 집단의 평균 차이가 통계적으로 유의미한지 판단하는 통계법

# 1. 일표본(표본vs알려진 모집단 평균)
# ex. 써브웨이 초코칩vs초코칩(알려진 모집단 평균)
# 2 도립표본( 서로 다른 두 집단 비교)
# ex. 남녀 영어점수 비교, A팀과 B팀 영업실적
# 3. 대응표본(동일 집단의 전후 비교)
# ex. 다이어트 프로젝트에 참여한 20대 회사원 운동 전후

